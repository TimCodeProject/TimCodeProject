import tkinter as tk
from tkinter import ttk
from tkinter import simpledialog

def создать_главное_окно():
    """Создаёт главное окно приложения."""
    окно = tk.Tk()
    окно.title("Пример таблицы в Tkinter")
    окно.geometry("600x400")
    return окно

def вычислить_значение(значение):
    """Вычисляет значение, если это выражение или функция."""
    try:
        # Если значение начинается с 'func_', вызываем функцию
        if isinstance(значение, str) and значение.startswith("func_"):
            return eval(значение)
        # Если это выражение, вычисляем его
        return eval(значение)
    except Exception as e:
        return f"Ошибка: {str(e)}"

def создать_таблицу(окно, данные, изменяемая=False, цвет="white"):
    """Создаёт таблицу с помощью виджета Treeview на основе данных."""
    таблица = ttk.Treeview(окно, show='headings')

    # Получение заголовков столбцов
    заголовки = list(данные[0].keys())
    таблица["columns"] = заголовки

    # Настройка заголовков и ширины столбцов
    for заголовок in заголовки:
        таблица.heading(заголовок, text=заголовок)
        таблица.column(заголовок, width=100)

    # Добавление строк в таблицу
    for строка in данные:
        # Для каждой строки проверяем значения
        строки_для_добавления = []
        for ключ, значение in строка.items():
            if isinstance(значение, list):  # Если значение - это список
                for val in значение:
                    строки_для_добавления.append({ключ: вычислить_значение(val)})  # Добавляем каждое значение в новую строку
            else:
                строки_для_добавления.append({ключ: вычислить_значение(значение)})  # Добавляем отдельное значение

        # Вставляем строки в таблицу
        for добавленная_строка in строки_для_добавления:
            таблица.insert("", "end", values=[добавленная_строка.get(заголовок, "") for заголовок in заголовки])

    таблица.pack(fill=tk.BOTH, expand=True)

    if изменяемая:
        таблица.bind("<Double-1>", редактировать_ячею)

    return таблица

def редактировать_ячею(event):
    """Редактирует ячейку таблицы при двойном щелчке."""
    выбранная_строка = таблица.selection()[0]
    колонка = таблица.identify_column(event.x)
    колонка_index = int(колонка.replace("#", "")) - 1
    старое_значение = таблица.item(выбранная_строка)['values'][колонка_index]

    новое_значение = simpledialog.askstring("Изменить значение", "Введите новое значение:", initialvalue=старое_значение)
    if новое_значение is not None:
        новые_значения = list(таблица.item(выбранная_строка)['values'])
        новые_значения[колонка_index] = вычислить_значение(новое_значение)  # Вычисляем новое значение
        таблица.item(выбранная_строка, values=новые_значения)

def добавить_строку(таблица, имя, возраст, город):
    """Добавляет строку в таблицу."""
    таблица.insert("", "end", values=(имя, возраст, город))

def удалить_строку(таблица):
    """Удаляет выделенную строку из таблицы."""
    выбранная_строка = таблица.selection()
    if выбранная_строка:
        таблица.delete(выбранная_строка)

def получить_значение(таблица, заголовок, строка_index):
    """Получает значение из таблицы по столбцу и заголовку."""
    заголовки = таблица["columns"]
    if заголовок in заголовки:
        колонка_index = заголовки.index(заголовок)
        значения = таблица.item(таблица.get_children()[строка_index])['values']
        return значения[колонка_index]
    else:
        return None

def создать_кнопки(окно, таблица):
    """Создаёт кнопки для добавления и удаления строк."""
    кнопка_добавить = tk.Button(окно, text="Добавить строку", command=lambda: добавить_строку(таблица, "Иван", 25, "Москва"))
    кнопка_удалить = tk.Button(окно, text="Удалить строку", command=lambda: удалить_строку(таблица))
    кнопка_получить = tk.Button(окно, text="Получить значение", command=lambda: print(получить_значение(таблица, "Имя", 0)))

    кнопка_добавить.pack(pady=10)
    кнопка_удалить.pack(pady=10)
    кнопка_получить.pack(pady=10)

def func_пример():
    """Пример функции, которую можно использовать в таблице."""
    return "Функция выполнена!"

def main():
    """Основная функция для запуска приложения."""
    окно = создать_главное_окно()

    # Данные для таблицы
    данные = [
        {"Имя": "Иван", "Возраст": 25, "Город": "Москва"},
        {"Имя": "Анна", "Возраст": 30, "Город": "Санкт-Петербург"},
        {"Имя": "Петр", "Возраст": [20, 22, 24], "Город": "Казань"},  # Пример с списком
        {"Имя": "Мария", "Возраст": "5 + 10", "Город": "Екатеринбург"},  # Пример с математическим выражением
        {"Имя": "Алексей", "Возраст": "func_пример()", "Город": "Тверь"},  # Пример использования функции
    ]

    таблица = создать_таблицу(окно, данные, изменяемая=True, цвет="lightgray")
    создать_кнопки(окно, таблица)

    окно.mainloop()

if __name__ == "__main__":
    main()
