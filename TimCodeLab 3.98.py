    def on_key_press(self, event):
        if event.keysym not in ["Control_L", "Control_R", "z", "y"]:
            self.undo_stack.append(self.text.get("1.0", tk.END))
            self.redo_stack.clear()
    
    def undo(self):
        if self.undo_stack:
            current_text = self.text.get("1.0", tk.END)
            self.redo_stack.append(current_text)
            previous_text = self.undo_stack.pop()
            self.text.delete("1.0", tk.END)
            self.text.insert("1.0", previous_text)
    
    def redo(self):
        if self.redo_stack:
            current_text = self.text.get("1.0", tk.END)
            self.undo_stack.append(current_text)
            next_text = self.redo_stack.pop()
            self.text.delete("1.0", tk.END)
            self.text.insert("1.0", next_text)
        
