def eval_timcode(code):
    global replacements
    
    def replace_command(match):
        if match.group(1) is not None:
            return match.group(1)
        return replacements[match.group(2)]

    command_pattern = (
        r"('(?:[^'\\]|\\.)*')"
        r"|(\b(" + '|'.join(re.escape(key) for key in replacements.keys()) + r")\b)(?!')"   
    )

    code = re.sub(command_pattern, replace_command, code, flags=re.DOTALL)
    
    try:
        exec_globals = {
            'set_variable': set_variable,
            'user_input': user_input,
            'variables': variables,
            'messagebox': messagebox,
            'show_help': show_help
        }
        exec(code, exec_globals)
    except Exception as e:
        messagebox.showerror("Ошибка выполнения", str(e))

