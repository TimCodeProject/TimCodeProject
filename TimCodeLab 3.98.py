    def open_colors_list(self):
        """Открывает окно выбора цвета"""
        color_window = tk.Toplevel(self.master, bg='#2E2E2E')
        color_window.title("Выбор цвета")
        color_window.geometry("300x400")
        
        # Listbox с цветами
