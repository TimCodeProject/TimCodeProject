    def open_file(self):
        file_path = filedialog.askopenfilename(defaultextension=".tim", filetypes=[("TimCode files", "*.tim")])
        if file_path:
            with open(file_path, 'r', encoding='utf-8') as f:
                self.text.delete(1.0, tk.END)
                self.text.insert(tk.END, f.read())
            self.current_file = file_path
            self.update_status()
        
